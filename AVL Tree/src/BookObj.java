public class BookObj 
{
	String Title;
	String lastName;
	
	//default constructor
	public BookObj()
	{
		Title ="";
		lastName ="";
		
	}
	
	//overloaded constructor
	public BookObj(String t,String l)
	{
		Title =t;
		lastName =l;
		
	}
}
